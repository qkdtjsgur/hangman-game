import java.util.ArrayList;
import java.util.List;
import java.util.Random;

/**
 * WordBank 클래스 - 단어 목록 관리
 * 객체지향: 단일 책임 원칙 (단어 관리만 담당)
 */
public class WordBank {

    private final List<Word> words = new ArrayList<>();
    private final Random random    = new Random();

    public WordBank() {
        initWords();
    }

    private void initWords() {
        // 동물
        String[][] data = {
            {"사자","동물"},{"호랑이","동물"},{"코끼리","동물"},{"기린","동물"},
            {"펭귄","동물"},{"돌고래","동물"},{"치타","동물"},{"늑대","동물"},
            {"독수리","동물"},{"토끼","동물"},{"여우","동물"},{"다람쥐","동물"},
            // 과일
            {"사과","과일"},{"바나나","과일"},{"포도","과일"},{"수박","과일"},
            {"딸기","과일"},{"복숭아","과일"},{"키위","과일"},{"망고","과일"},
            // 자연
            {"하늘","자연"},{"바다","자연"},{"구름","자연"},{"바람","자연"},
            {"폭포","자연"},{"무지개","자연"},{"번개","자연"},
            // 도시
            {"서울","도시"},{"부산","도시"},{"제주","도시"},{"인천","도시"},
            // 스포츠
            {"축구","스포츠"},{"야구","스포츠"},{"농구","스포츠"},
            {"태권도","스포츠"},{"수영","스포츠"},{"배드민턴","스포츠"},
            // 음식
            {"김치","음식"},{"비빔밥","음식"},{"삼겹살","음식"},
            {"떡볶이","음식"},{"냉면","음식"},{"된장찌개","음식"},
            // 악기
            {"피아노","악기"},{"기타","악기"},{"바이올린","악기"},
            {"드럼","악기"},{"플루트","악기"},
            // 계절
            {"봄","계절"},{"여름","계절"},{"가을","계절"},{"겨울","계절"}
        };

        for (String[] entry : data) {
            words.add(new Word(entry[0], entry[1]));
        }
    }

    /** 랜덤 단어 반환 */
    public Word getRandomWord() {
        return words.get(random.nextInt(words.size()));
    }

    public int size() { return words.size(); }
}
